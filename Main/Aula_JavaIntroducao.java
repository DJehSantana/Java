package Main;

public class Aula_JavaIntroducao {
    
        public void Main() {
        }
    
        public static void main (String[] args) {
    
            String nome = "Jéssica";
    
            System.out.println("Olá, " + nome);

            int a = 16;
            int b = 5;
            
            int soma = a + b;
            int subtracao = a - b;
            int multiplicacao = a * b;
            float divisao = (float) a / b;

            System.out.println("Trabalhando com operações:");
            System.out.println("Soma: a + b = " + soma);
            System.out.println("Subtração: a - b = " + subtracao);
            System.out.println("Multiplicação: a * b = " + multiplicacao);
            System.out.println("Divisão: a / b = " + divisao);

        }    
    
}
